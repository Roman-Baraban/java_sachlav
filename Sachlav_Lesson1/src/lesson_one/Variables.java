package lesson_one;

public class Variables {
    public static void main(String[] args) {
        byte myByte = 2;
        byte myByte_1 = 4;

        short myShort = 33;
        short myShort_1 = 34;

        int myInt = 444;
        int myInt_1 = 4444;

        long myLong = 55554524523453153L;
        long myLong_1 = 55554524524453153L;

        System.out.println(myByte);
        System.out.println(myByte_1);
        System.out.println(myShort);
        System.out.println(myShort_1);
        System.out.println(myInt);
        System.out.println(myInt_1);
        System.out.println(myLong);
        System.out.println(myLong_1);
        System.out.println();

        float myFloat = 5.35f;
        float myFloat_1 = 5.34f;

        double myDouble = 5.34;
        double myDouble_1 = 5.35;

        boolean myTrue = true;
        boolean myTrue_1 = false;

        System.out.println(myFloat);
        System.out.println(myFloat_1);
        System.out.println(myDouble);
        System.out.println(myDouble_1);
        System.out.println(myTrue);
        System.out.println(myTrue_1);
        System.out.println();

        char myChar = 95;
        char myChar_1 = 'b';
        char myChar_2 = 0x42;
        char myChar_3 = 0x0049;
        char myChar_4 = '\u0046';

        System.out.println(myChar);
        System.out.println(myChar_1);
        System.out.println(myChar_2);
        System.out.println(myChar_3);
        System.out.println(myChar_4);
        System.out.println();

    }
}
